// Create board
function createBoard () {
  for (let i = 0; i < layout.length; i++) {
    const square = document.createElement('div')
    grid.appendChild(square)
    squares.push(square)

    if (layout[i] === 0) {
      squares[i].classList.add('pac-dot')
    } else if (layout[i] === 1) {
      squares[i].classList.add('wall')
    } else if (layout[i] === 2) {
      squares[i].classList.add('ghost-lair')
    } else if (layout[i] === 3) {
      squares[i].classList.add('power-pellet')
    }
  }
}

createBoard()

// Starting position of pacman
let pacmanCurrentIndex = 490

squares[pacmanCurrentIndex].classList.add('pacman')

function control (e) {
  squares[pacmanCurrentIndex].classList.remove('pacman')
  switch (e.keyCode) {
    case 40:
      console.log('pressed down')
      if (
        !squares[pacmanCurrentIndex + width].classList.contains('ghost-lair') &&
        !squares[pacmanCurrentIndex + width].classList.contains('wall') &&
        pacmanCurrentIndex + width < width * width
      ) {
        pacmanCurrentIndex += width
      }

      break
    case 38:
      console.log('pressed up')
      if (
        !squares[pacmanCurrentIndex - width].classList.contains('ghost-lair') &&
        !squares[pacmanCurrentIndex - width].classList.contains('wall') &&
        pacmanCurrentIndex - width >= 0
      ) {
        pacmanCurrentIndex -= width
      }

      break
    case 37:
      console.log('pressed left')
      if (
        !squares[pacmanCurrentIndex - 1].classList.contains('ghost-lair') &&
        !squares[pacmanCurrentIndex - 1].classList.contains('wall') &&
        pacmanCurrentIndex % width !== 0
      ) {
        pacmanCurrentIndex -= 1
        if (pacmanCurrentIndex === 364) {
          pacmanCurrentIndex = 391
        }
      }

      break
    case 39:
      console.log('pressed right')
      if (
        !squares[pacmanCurrentIndex + 1].classList.contains('ghost-lair') &&
        !squares[pacmanCurrentIndex + 1].classList.contains('wall') &&
        pacmanCurrentIndex % width < width - 1
      ) {
        pacmanCurrentIndex += 1
        if (pacmanCurrentIndex === 391) {
          pacmanCurrentIndex = 364
        }
      }

      break
  }

  squares[pacmanCurrentIndex].classList.add('pacman')
  pacDotEaten()
}

document.addEventListener('keyup', control)

function pacDotEaten () {
  if (squares[pacmanCurrentIndex].classList.contains('pac-dot')) {
    score++
    squares[pacmanCurrentIndex].classList.remove('pac-dot')
    scoreDisplay.innerHTML = score
  }
}

class Ghost {
  constructor (className, startIndex, speed) {
    this.className = className
    this.startIndex = startIndex
    this.speed = speed
    this.currenIndex = startIndex
    this.isScared = false
    this.timerId = NaN
  }
}

const ghosts = [
  new Ghost('blinky', 348, 250),
  new Ghost('pinky', 376, 400),
  new Ghost('inky', 351, 300),
  new Ghost('clyde', 379, 500)
]

// Draw my ghosts onto my grid

ghosts.forEach(ghost => {
  squares[ghost.currenIndex].classList.add(ghost.className)
  squares[ghost.currenIndex].classList.add('ghost')
})

// ?ove the ghost
ghosts.forEach(ghost => moveGhost(ghost))

function moveGhost (ghost) {
  console.log('moved ghost')
  const directions = [-1, +1, -width, +width]
  let direction = directions[Math.floor(Math.random() * directions.length)]
  console.log(direction)

  ghost.timerId = setInterval(function () {
    if (
      !squares[ghost.currenIndex + direction].classList.contains('wall') &&
      !squares[ghost.currenIndex + direction].classList.contains('ghost')
    ) {
      squares[ghost.currenIndex].classList.remove(ghost.className)
      squares[ghost.currenIndex].classList.remove('ghost')

      ghost.currenIndex += direction

      squares[ghost.currenIndex].classList.add(ghost.className)
      squares[ghost.currenIndex].classList.add('ghost')
    } else direction = directions[Math.floor(Math.random() * directions.length)]
  }, ghost.speed)
}
